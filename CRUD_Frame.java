    private void btn_cariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cariActionPerformed
      try {
            String search = txt_cari.getText();
            String sql = "SELECT * FROM siswa WHERE id like '%" + search + "%' OR nama like '%" + search + "%'";
            PreparedStatement ps = null;
            Object[] row = {"id", "nama", "alamat"};
            DefaultTableModel dtm = new DefaultTableModel(null, row);
            tabel_siswa.setModel(dtm);

            ResultSet rs = null;
            Koneksi connection = new Koneksi();
            ps = connection.getKoneksi().prepareStatement(sql);
            rs = ps.executeQuery();
            while (rs.next()) {
                String id = rs.getString(1);
                String nama = rs.getString(2);
                String alamat = rs.getString(3);
              
                String[] baris = {id,nama,alamat};
                dtm.addRow(baris);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Data yang anda cari tidak dapat ditemukan " + e.getMessage());
        }
    }//GEN-LAST:event_btn_cariActionPerformed

